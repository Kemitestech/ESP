              <h1 id="sec0"><small>The Church</small></h1>
              <p>Celestial Church of Christ is a spiritual, world-wide, united, indivisible Holy Church which came into the world from heaven by DIVINE ORDER on the 29th of September 1947 in Porto Novo, Republic of Benin through the founder of the Church, the Late Reverend, Pastor, Prophet, <a data-toggle="modal" data-target="#myModal"><b>Founder Samuel Bilehou Joseph Oshoffa</b></a>. The Church is well known with Parishes, Dioceses all over the world with its International Headquarters in Nigeria.  See Celestial History for Brief History of the Church.</p>
              <hr>
              <h1 id="sec1"><small>The Divine Order</small></h1>
              <p>God called Rev., Pastor, Prophet Oshoffa on the day of total Eclipse of the sun in West Africa, when he was in the forest of Dahomey (now Republic of Benin), to purchase Ebony wood being a carpenter and timber trader, just like his father.  He was in the wilderness for forty days and forty nights.  Just as our Lord Jesus was led by the spirit into the wilderness for forty days and forty nights.  St. Matt. 4: 1-11, Mark 1:12-13, Luke 4:1-13.  Just as our Lord started his mission of redemption alone, so also did our highly esteemed Rev., Pastor, Founder, S.B.J. Oshoffa started alone, on the 29th September 1947.</p>
              <hr>
              <h1 id="sec2"><small>The Spiritual Message</small></h1>
              <p>In the deep mystery of divine appearance, during prayer on the 19th September 1947, a winged Angel bathed in intense light, word came from God to the founder as follows: <br><br>"It is the wish of God to send you on an errand of preaching into the World.   Many nominal Christians there who were confronted by difficulties and problems of this world.  They run after fetish priests and other power of darkness for all kinds of assistance.  Consequently, on their death, they cannot see Christ because, by their action, Satan has left his spiritual mark on them.  To assist you in in your work so that men may listen to the fact that God has sent you."</p>
              <hr>
              <h1 id="sec3"><small>Tenet and Mode of Worship</small></h1>
              <p>
              The Church under the absolute dictatorship of Holy Spirit is hereby emphasized for:<br><br>
              <b>The Name of the Church:</b><br><br>
              The name of the Church "Celestial Church of Christ" was revealed by the Holy Spirit through a Prophet who was held in trance for seven days.<br><br>
              <b>The Tenets and Mode of Worship of the Church:</b><br><br>
              Ordained by the Holy Spirit.  The rules and regulations written in the booklet of "Tents and Admission into the membership of Celestial Church of Christ."<br><br>
              Mode of worship includes the setting of the Altar, and the seat therein, the number of candles used for various types of services and the seating arrangement in particular, it should be noted that the seating arrangements was revealed through a Prophetess who under the influence of the Holy Spirit in the wilderness on Friday 5th of October, 1947, sketched the seating arrangement using oranges. 
              <br><br>
              <b>The Service of the Church:</b><br><br>
              Revealed through the Holy Spirit as promised by our Lord Jesus Christ (John 14:25-26)<br><br>
              There is an order of service laid down in the Celestial Church of Christ for different occasions such as Marriage, Funeral, Naming Ceremony, Mass in Remembrance of the dead, etc.  The order for the respective occasion is strictly as revealed by the Holy Spirit and as set out in the booklet titled "Order of Service".</p>
              <hr>
              <h1 id="sec4"><small>Holy Rites or Sacraments</small></h1>
              <p>There are a number of Holy Rites or Sacrament within the form of worship in Celestial Church of Christ utmost importance.  Members shall avail themselves of the opportunity to partake in these rites or sacraments: Baptism, Holy Communion, Annual Washing of Feet, Annual Pilgrimage to Imeko, Ogun State, Nigeria at Christmas Eve, Annual Harvest Thanksgiving Service, and Holy Mary's Day.</p>
              <hr>
              <h1 id="sec5"><small>Membership</small></h1>
              <p>All persons who earnestly, sincerely desire to be saved looking for salvation and therefore accept Jesus Christ as the son of God and Holy conversion by taking up such duties and privileges as entailed in the fellowship and the Ten Commandments are welcome into the fold.  Celestial Church of Christ is an inclusive congregation, with the desire to observe the Tenets and Mode of Worship is welcome.</p><br><br>
              <p><b>May God Bless you (Amen).</b></p>
