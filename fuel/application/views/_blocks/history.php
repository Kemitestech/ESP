      <h1 id="sec01"><small>The Beginning</small></h1>
      <p>
      Edward Street Parish came into being after the fire incident that engulfed and destroyed Harton Street Parish, the mother parish in January 1990. (Harton Street Parish is the international headquarters and the first celestial church of Christ in the overseas.
      </p>
      <p>
      Edward Street Parish is the product of the efforts of some God fearing and hardworking ministers, officers and members, who after the fire, were determined not to be cowed and broken by the unfortunate incident. They were prepared to forge ahead with belief and faith in God to ensure continuity and minimise any break in service-worship. They knew that any break in service-worship could dampen interest and morale and lead to loss of members. With these in mind, they went in search of an alternative place of worship.
      </p>
      <p>
        After briefly holding services and worshipping at St Lukes church on Evelyn Street and Evelyn community centre both in Deptford, the lord led us to discover St Marks building. After a brief enquiry, we met the Rector Reverend father Diamond, with whom we discussed and negotiated the terms for securing the top floor of the building but not before he demanded to meet with our leader and Shepherd (then Senior Evangelist Salu) as a condition for handing over the key. A condition we complied with to make St. Mark’s Centre our home, the home of Edward Street Parish.
      </p>
      <hr>
      <h1 id="sec02"><small>Growth/Achievements</small></h1>
      <p>
      Our journey to St. Mark’s had the hand of God written all over it, when we moved into the building, we found ourselves sharing with other tenants, the ground floor was being used as a Youth Club and as a regular meeting place for the Lodge fraternity.
       We however held our first service worship on the 21st of February 1990 and ever since our story has been marked by tremendous successes.
      </p>
      <p>
      From a humble beginning of renting only the vacant top floor in 1990, we progressed (under the leadership of Superior Evangelist F.A.O Salu) to secure the use of the ground floor after the youth club ceased operation due to lack of funds and the Lodge fraternity decided they had enough of us and moved out.
      </p>
      <p>
      In 1996, after much persuasion and God helping us to ward off competition from Developers and other interested parties (other churches were also interested in the building), St Marks authority led by Rev Peter Fellows and Mrs Theresa Franks (Mother Theresa) decided to sell the church building along with the parcel of land at the rear  (on which the vicarage stood before it was pulled down) to us.
      </p>
      <p>
      By 1998, precisely the 20th day of the eleventh month (20th November), the property changed hands and St Marks building became the exclusive property of Celestial Church of Christ, Edward Street Parish. In 2002, after a meeting of the shepherd with the Parochial Committee, some church elders and Lana Ajibade associate, It was decided that the parcel of land at the rear of the church be developed to provide housing and help with problem of homelessness in the community.
      </p>
      <p>
      After an initial Planning permission application was refused in 2002, another application was resubmitted in 2003 and finally approved in 2004 for the construction of an ultra- modern building containing eleven (11) flats.
      Work eventually started in January 2006 after a lot of ground work had been carried out and precisely one full year later our dream was realised as work was completed and LULI Court was born! In the same year, the outstanding mortgage on the main church building was paid off. Thanks to God!
      </p>
      <p><b>May the Lord God continue to lead us aright (Amen).</b></p>
