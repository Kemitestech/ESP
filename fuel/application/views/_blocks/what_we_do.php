			<h2 class="header-title">Christian Objects</h2>
			<p>
				As a Christian charity, Edward Street Parish has a set of religious and charity objects that informs its function and operation. The objects are clearly set out in its governing document known as the ‘charity constitution’. It includes among others
			</p>
			<ol type="a">
			  <li>
					<p>
						To further the Christian religion and their charitable work by propagating the doctrines of the charity and the teaching of our Lord Jesus Christ throughout the whole world and particularly in London and the United Kingdome.
   				</p>
				</li>
			  <li>
					<p>
						To relieve sickness and financial hardship and to promote and preserve good health through the provision of funds, goods and services of any kind.
					</p>
				</li>
			  <li>
					<p>
						To advance education and provide or assist in the organisation or provision of facilities in the interest of education and social welfare in any part of the area of interest.
					</p>
				</li>
			</ol>
			<p>The full list can be found in the governing document.</p>
			<hr>
			<h2 class="header-title">Membership</h2>
			<p>
				The current membership of the parish is about 600 strong, made up of 290 female adults, 240 adult males and 70 youths and children. Membership of the church/parish is open to any interested member who is prepared to promote or help promote the objects of the charity. It is open to anyone who earnestly and sincerely desire to be saved; Accepts Jesus Christ as the son of God and the Lord and saviour of mankind and wish to worship him in the parish/church/charity premises.
			</p>
			<p>
				A member must subscribe and adhere to (in belief and lifestyle) the statement of belief and tenets of the church/ charity and the Holy Bible. He or she must have been attending at church services for at least one year and must be duly registered as a member by going through the registration process as laid down by the parochial committee/trustee. A prospective member must also agree to be baptised in the church irrespective of previous baptisms.
			</p>
			<hr>
			<h2 class="header-title">Spiritual and Administrative Structure</h2>
			<p>
				The management structure adopted by the parish is divided into two parts, the Spiritual Leadership and the Administrative Leadership.
			</p>
			<h3 class="header-title">Spiritual Leadership</h3>
			<p>
				This is headed by the Shepherd in charge or Minister in charge, usually the most senior in spiritual rank, He is assisted in the spiritual role by an assistant shepherd (If there is one in place), the Clergies and Elders of the church.
			</p>
			<p>
				He is responsible for all spiritual affairs and spiritual growth of the parish as in leading the service of worship and preaching sermons in accordance with the Christian faith. All the operational duties of the following departments/sections are under his charge and purview
			</p>
			<ol type="a">
				<li>
					<p>The choristers and choir ministry</p>
				</li>
				<li>
					<p>The Prophets and prophetesses ministry</p>
				</li>
				<li>
					<p>Sunday School</p>
				</li>
				<li>
					<p>Bible class and Evangeline ministry</p>
				</li>
			</ol>
			<p>
				The shepherd in charge or the Assitant shepherd in charge are not meant to meddle in the financial matters of the church apart from giving advice when necessary but must be consulted on all financial decision or actions of the administrative arm.
			</p>
			<h3 class="header-title">The Administrative leadership</h3>
			<p>
				The administrative leadership of the parish/church rest with the parochial committee and the Trustees known as the 'charity trustees’ body' and are responsible for the control and management of the Parish/Church.
			</p>
			<ol type="a">
				<li>
					<p>The Parochial Committee<br>This is the ruling body of the Parish responsible for its day to day running. It is made up of between 20 and 33 members of which 10 are democratically elected executive officers as follows</p>
					<ul>
						<li><p>Chairman and two vice chairman</p></li>
						<li><p>Hon. Secretary and two Deputies</p></li>
						<li><p>Treasurer and one Deputy</p></li>
						<li><p>Financial secretary and one Deputy</p></li>
					</ul>
					<p>The composition of the full membership of the committee are set out in the governing document. All members of the committee both elected and non-elected hold office for a two year term after which they may be re-elected for a further period of term of office.</p>
				</li>
				<li>
					<p>The Trustees</p>
					<p>These comprise of the three principal officers of the parochial committee (i.e. chairman, secretary and treasurer) and four ex-officio nominated members, chosen from amongst the senior members of the church on the basis of maturity, experience and continuity.</p>
					<p>They are responsible for all legal matters relating to the Parish (Church/ charity).  All Estate properties, Chattels and Securities of the Parish/church are administered and managed by this body. As trustees, they have the power to acquire, dispose, transfer and or mortgage the Landed property of the Parish (Church/Charity).</p>
				</li>
			</ol>
